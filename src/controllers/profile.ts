export const getPlaylistCollection: RequestHandler = async (req, res) => {
  const { limit = "20", pageNo = "0" } = req.query as paginationQuery;
  const { playlistId } = req.params;

  if (!isValidObjectId(playlistId)) {
    res.status(422).json({
      error: "invalid playlistID!",
      message: "invalid id!",
    });
    return;
  }
  const aggregationLogic = [
    {
      $match: {
        _id: new Types.ObjectId(playlistId),
        visibility: { $ne: "private" },
      },
    },
    {
      $project: {
        items: {
          $slice: [
            "$items",
            parseInt(pageNo) * parseInt(limit),
            parseInt(limit),
          ],
        },
        title: "$title",
      },
    },
    {
      $unwind: "$items",
    },
    {
      $lookup: {
        from: "cardscollections",
        localField: "items",
        foreignField: "_id",
        as: "collections",
      },
    },
    {
      $unwind: "$collections",
    },
    {
      $lookup: {
        from: "users",
        localField: "collections.owner",
        foreignField: "_id",
        as: "userInfo",
      },
    },
    { $unwind: "$userInfo" },
    {
      $group: {
        _id: {
          id: "$_id",
          title: "$title",
        },
        collections: {
          $push: {
            id: "$collections._id",
            title: "$collections.title",
            description: "$collections.description",
            poster: "$collections.poster.url",
            owner: {
              name: "$userInfo.name",
              id: "$userInfo._id",
            },
          },
        },
      },
    },
    {
      $project: {
        _id: 0,
        id: "$_id.id",
        title: "$_id.title",
        collections: "$$ROOT.collections",
      },
    },
  ];

  const [playlistResult] = await playlist.aggregate(aggregationLogic);
  if (!playlistResult) {
    const [autoPlaylistResult] = await AutoGeneratedPlaylist.aggregate(
      aggregationLogic
    );
    res.json({ list: autoPlaylistResult });
    return;
  }
  res.json({ list: playlistResult });
};
